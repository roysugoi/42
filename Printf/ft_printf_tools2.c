#include "ft_printf.h"

int		ft_atoi(const char *str)
{
	int					i;
	unsigned long long	n;

	i = 0;
	n = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + str[i] - '0';
		i++;
	}
	return (n);
}

int		ft_atoi_counter(int i)
{
	int j;

	j = 0;
	while (i / 10 >= 1)
		j++;
	j++;
	return (j);
}
